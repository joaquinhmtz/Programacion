public class Var_Globales{

public static byte var_byte;
public static short var_short;
public static int var_int;
public static long var_long;
public static double var_double;
public static char var_char;
public static boolean var_boolean;
public static String var_string;

	public static void main (String[] args){


	System.out.println("Valor de byte: " + var_byte);
	System.out.println("Valor de short: " +var_short);
	System.out.println("Valor de int: " +var_int);
	System.out.println("Valor de long: " +var_long +"L");
	System.out.println("Valor de double: " +var_double +"d");
	System.out.println("Valor de char: " +var_char +"\u0000");
	System.out.println("Valor de boolean: " +var_boolean);
	System.out.println("Valor de String: " + var_string);



	}
}