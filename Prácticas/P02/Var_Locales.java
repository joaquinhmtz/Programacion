public class Var_Locales{
		public static void main (String[] args){

	byte var_byte = 127;
	short var_short = 32767;
	int var_int = 2147483647;
	long var_long = 9223372036854775807L;
	char var_char = '2';
	String var_string = null;
	String var_string2 = "hola java";

	System.out.println("Valor de byte: " + var_byte);
	System.out.println("Valor de short: " + var_short);
	System.out.println("Valor de int: " + var_int);
	System.out.println("Valor de long: " + var_long);
	System.out.println("Valor de char: " + var_char);
	System.out.println("Valor de String: " + var_string);
	System.out.println("Valor de String: " + var_string2);
	}
}